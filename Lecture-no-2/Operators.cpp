// Arithmetic :  + , -, *

#include <iostream>
using namespace std;

int main() {
    int a = 5 , b = 10;

    // sum = a + b;
    cout << "Sum = "<< (a+b) << endl;
    cout << "Sub = "<< (a-b) << endl;
    cout << "Mul = "<< (a*b) << endl;
    cout << "div = "<< (a/b) << endl;
    cout << "Reminder = "<< (a%b) << endl;

    cout << (3 < 5) << endl;
    cout << (3 > 5) << endl;
    cout << (3 <= 3) << endl;

    return 0;
}